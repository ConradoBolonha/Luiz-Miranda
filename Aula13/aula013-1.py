
###  FATIAMENTO DE STRING  ###

texto = 'Python Brasil'

print(texto[3]) #  Vai imprimir a letra "h"
print(texto[:-1]) #  Vai imprimir o texto todo excluindo a letra "l" (Python Brasi)
print(texto[:-5]) #  Vai imprimir o texto todo excluindo as letras "rasil" (Python B)
print(texto[2:5]) #  Vai imprimir "tho"
print(texto[:4]) # Vai imprimir "Pyth"
print(texto[0:4]) # Vai imprimir "Pyth"
print(texto[7:]) #  Vai imprimir a palavra "Brasil"
print(texto[-13:-7]) #  Vai imprimir a palavra "Python"
print(texto[::2]) #  Imprime "Pto rsl". [::2] pula de 2 em 2
print(texto[7::2]) # Imprime "Bai". Começa do caracter 7 e vai até o fim pulando de 2 em 2 
